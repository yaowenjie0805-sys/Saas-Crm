import {
  useContractPaymentCrudActions,
  useCustomerPipelineCrudActions,
  useFollowUpContactTaskCrudActions,
} from './crudActions'
import { useLeadCrudActions } from './useLeadCrudActions'

export function useAppCrudActions({
  authToken,
  lang,
  t,
  canWrite,
  canDeleteCustomer,
  canDeleteOpportunity,
  handleError,
  setCrudError,
  setCrudFieldError,
  pickFieldErrors,
  formatValidation,
  validateCustomerForm,
  validateContactForm,
  validateContractForm,
  validatePaymentForm,
  leadForm,
  setLeadForm,
  leads,
  customerForm,
  setCustomerForm,
  customerPage,
  setCustomerPage,
  customers,
  opportunityForm,
  setOpportunityForm,
  opportunityPage,
  setOpportunityPage,
  opportunities,
  followUpForm,
  setFollowUpForm,
  contactForm,
  setContactForm,
  contactPage,
  setContactPage,
  contacts,
  contractForm,
  setContractForm,
  contractPage,
  setContractPage,
  contracts,
  paymentForm,
  setPaymentForm,
  paymentPage,
  setPaymentPage,
  payments,
  refreshPage,
  setQuotePrefill,
  onLeadConvertedToQuote,
}) {
  const customerPipelineCrudActions = useCustomerPipelineCrudActions({
    authToken,
    lang,
    canWrite,
    canDeleteCustomer,
    canDeleteOpportunity,
    handleError,
    setCrudError,
    setCrudFieldError,
    pickFieldErrors,
    formatValidation,
    validateCustomerForm,
    customerForm,
    setCustomerForm,
    customerPage,
    setCustomerPage,
    customers,
    opportunityForm,
    setOpportunityForm,
    opportunityPage,
    setOpportunityPage,
    opportunities,
    refreshPage,
  })
  const leadCrudActions = useLeadCrudActions({
    authToken,
    lang,
    t,
    canWrite,
    handleError,
    formatValidation,
    pickFieldErrors,
    setCrudError,
    setCrudFieldError,
    leadForm,
    setLeadForm,
    leads,
    refreshPage,
    setQuotePrefill,
    onLeadConvertedToQuote,
  })
  const followUpContactTaskCrudActions = useFollowUpContactTaskCrudActions({
    authToken,
    lang,
    canWrite,
    handleError,
    setCrudError,
    setCrudFieldError,
    pickFieldErrors,
    formatValidation,
    validateContactForm,
    followUpForm,
    setFollowUpForm,
    contactForm,
    setContactForm,
    contactPage,
    setContactPage,
    contacts,
    refreshPage,
  })
  const contractPaymentCrudActions = useContractPaymentCrudActions({
    authToken,
    lang,
    canWrite,
    canDeleteCustomer,
    handleError,
    setCrudError,
    setCrudFieldError,
    pickFieldErrors,
    formatValidation,
    validateContractForm,
    validatePaymentForm,
    contractForm,
    setContractForm,
    contractPage,
    setContractPage,
    contracts,
    paymentForm,
    setPaymentForm,
    paymentPage,
    setPaymentPage,
    payments,
    refreshPage,
  })

  return {
    ...leadCrudActions,
    ...followUpContactTaskCrudActions,
    ...customerPipelineCrudActions,
    ...contractPaymentCrudActions,
  }
}
