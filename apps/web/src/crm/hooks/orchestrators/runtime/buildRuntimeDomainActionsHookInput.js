import { buildRuntimeDomainActionsConfig } from './buildRuntimeDomainActionsConfig'
import {
  buildRuntimeDomainActionsState,
  buildRuntimeDomainActionsLoaders,
  buildRuntimeShellPrefetchBindings,
  buildRuntimeShellWorkbenchBindings,
  buildRuntimeShellPerfBindings,
} from './buildRuntimeDomainActionsHookBindings'

function buildRuntimeDomainActionsHookInput({
  auth,
  lang,
  role,
  t,
  canViewOpsMetrics,
  canManagePermissions,
  canViewReports,
  handleError,
  refreshPage,
  runtime,
  loadLeadAssignmentRules,
  loadAutomationRulesV1,
  loadPermissionConflicts,
  loadAdminUsers,
  loadApprovalTemplates,
  loadApprovalStats,
  loadApprovalInstances,
  loadApprovalTasks,
  loadApprovalDetail,
  loadNotificationJobs,
  loadTenants,
  loadApprovalTemplateVersions,
  hasInvalidAuditRange,
  pageSignature,
  customerQ,
  customerStatus,
  customerSize,
  canSkipFetch,
  loadCustomers,
  markFetched,
  loadLeads,
  leadQ,
  leadStatus,
  leadSize,
  loadOpportunities,
  loadTasks,
  loadWorkbenchToday,
  loadReports,
  markChunkPreloadHit,
  markNavStart,
  activePage,
  loadReasonRef,
  setActivePage,
  navigate,
  pageToPath,
  pageChunkPreloaders,
  pageDomainPreloaders,
  prefetchI18nNamespaces,
  markWorkbenchJumpDecision,
  markWorkbenchActionResult,
  setPaymentStatus,
  setContractStatus,
  setApprovalTaskStatus,
  setFollowCustomerId,
  setFollowQ,
  setFollowUpForm,
  setQuoteOpportunityFilter,
  setOrderOpportunityFilter,
  workbenchJumpRef,
  markWorkbenchJumpMeta,
  commerceDomain,
  markNavEnd,
  markRenderCommit,
  markFirstInteractive,
  getMetrics,
}) {
  return {
    ...buildRuntimeDomainActionsConfig({
      auth,
      lang,
      role,
      t,
      canViewOpsMetrics,
      canManagePermissions,
      canViewReports,
      handleError,
      refreshPage,
      state: buildRuntimeDomainActionsState(runtime),
      loaders: buildRuntimeDomainActionsLoaders({
        loadLeadAssignmentRules,
        loadAutomationRulesV1,
        loadPermissionConflicts,
        loadAdminUsers,
        loadApprovalTemplates,
        loadApprovalStats,
        loadApprovalInstances,
        loadApprovalTasks,
        loadApprovalDetail,
        loadNotificationJobs,
        loadTenants,
        loadApprovalTemplateVersions,
        hasInvalidAuditRange,
      }),
    }),
    shellBindings: {
      prefetch: buildRuntimeShellPrefetchBindings({
        auth,
        pageSignature,
        customerQ,
        customerStatus,
        customerSize,
        canSkipFetch,
        loadCustomers,
        markFetched,
        loadApprovalTemplates,
        loadApprovalStats,
        loadLeads,
        leadQ,
        leadStatus,
        leadSize,
        loadOpportunities,
        loadTasks,
        loadWorkbenchToday,
        loadReports,
        markChunkPreloadHit,
        markNavStart,
        activePage,
        loadReasonRef,
        setActivePage,
        navigate,
        pageToPath,
        pageChunkPreloaders,
        pageDomainPreloaders,
        prefetchI18nNamespaces,
      }),
      workbench: buildRuntimeShellWorkbenchBindings({
        activePage,
        setActivePage,
        navigate,
        pageToPath,
        markNavStart,
        markWorkbenchJumpDecision,
        markWorkbenchActionResult,
        loadReasonRef,
        setPaymentStatus,
        setContractStatus,
        setApprovalTaskStatus,
        setFollowCustomerId,
        setFollowQ,
        setFollowUpForm,
        setQuoteOpportunityFilter,
        setOrderOpportunityFilter,
        commerceDomain,
        runtime,
        leadSize,
        customerSize,
        workbenchJumpRef,
        markWorkbenchJumpMeta,
      }),
      perf: buildRuntimeShellPerfBindings({
        activePage,
        markNavEnd,
        markRenderCommit,
        markFirstInteractive,
        getMetrics,
        runtime,
      }),
    },
    setQuotePrefill: runtime.setQuotePrefill,
    refreshPage,
  }
}

export { buildRuntimeDomainActionsHookInput }
